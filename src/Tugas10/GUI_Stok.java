    public void tampil_hjm() {
        try {
            koneksi();
            String sql = "SELECT nim FROM tb_stok order by model asc";
            Statement stt = conn.createStatement();
            ResultSet res = stt.executeQuery(sql);
            while (res.next()) {
                Object[] ob = new Object[3];
                ob[0] = res.getString(1);
                cmbModel.addItem((String) ob[0]);
            }
            res.close();
            stt.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

    public void refresh() {
        new GUI_Stok().setVisible(true);
        this.setVisible(false);
    }

    public void insert() {
        String model = (String) cmbModel.getSelectedItem();
        String tanggal = txttgl.getText();
        String bulan = txtbln.getText();
        String tahun = txtthn.getText();
        try {
            koneksi();
            Statement statement = conn.createStatement();
            statement.executeUpdate("INSERT INTO tb_stok (model, tanggal,bulan, tahun)"
                    + "VALUES('" + model + "','" + tanggal + "','" + bulan + "','" + tahun + "')");
            statement.close();
            JOptionPane.showMessageDialog(null, "Berhasil Memasukan Data Peminjaman!" + "\n");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Input!");
        }
        refresh();
    }

    public void update() {
        String model = (String) cmbModel.getSelectedItem();
        String tanggal = txttgl.getText();
        String bulan = txtbln.getText();
        String tahun = txtthn.getText();
        try {
            Statement statement = conn.createStatement();
            statement.executeUpdate("UPDATE tb_stok SET model='" + model + "','" + tanggal + "','" + bulan + "','" + tahun + "')");
            statement.close();
            conn.close();
            JOptionPane.showMessageDialog(null, "Update Data Peminjaman Berhasil!");
        } catch (Exception e) {
            System.out.println("Error : " + e);
        }
        refresh();
    }

    public void delete() {
        int ok = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin akan menghapus data ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            try {
                String sql = "DELETE FROM tb_stok WHERE barang='" + txttgl.getText() + "'";
                java.sql.PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil di hapus");
                batal();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data gagal di hapus");
            }
        }
        refresh();
    }
    
    public void cari() {
        try {
            try ( Statement statement = conn.createStatement()) {
                String sql = "SELECT * FROM tb_stok WHERE `barang`  LIKE '%" + txt_search.getText() + "%'";
                ResultSet rs = statement.executeQuery(sql); //menampilkan data dari sql query
                if (rs.next())
                {
                    cmbModel.setSelectedItem(rs.getString(2));
                    txttgl.setText(rs.getString(3));
                    txtbln.setText(rs.getString(4));
                    txtthn.setText(rs.getString(5));
                } else {
                    JOptionPane.showMessageDialog(null, "Data yang Anda cari tidak ada");
                }
            }
        } catch (Exception ex) {
            System.out.println("Error." + ex);
        }
    }


